sub parse_num() {
    my $v = shift;
    $v =~ m/([-]{0,1})0\.(\d{9})(\d{9})D([-+]{1})(\d{2})/;
    print "is neg num?: " . (trim($1) eq '-' ? 'yes' : 'no') . "\n";
    print "mantissa1:   " . $2 . "\n";
    print "mantissa2:   " . $3 . "\n";
    print "is neg exp?: " . (trim($4) eq '-' ? 'yes' : 'no') . "\n";
    print "exponent:    " . $5 . "\n";
# mantissa1 * Math.pow(10, (exponent - 9)) + mantissa2 * Math.pow(10, (exponent - 18))

    my $mantissa1;
    my $mantissa2;
    if(trim($4) eq '+'){ 
	$mantissa1 = $2 ** ($5 - 9);
	$mantissa2 = $3 ** ($5 - 18);
    } else {
	$mantissa1 = $2 ** (($5 + 9)*-1);
	$mantissa2 = $3 ** (($5 + 18)*-1);
    }

    if(trim($1) eq '-') {
	return -1 * ($mantissa1 + $mantissa2);
    } else {
	return ($mantissa1 + $mantissa2);
    }
}


sub trim() {
    my $s = shift;
    $s =~ s/\s+$//g;
    $s =~ s/^\s+//g;
    local $/;
    chomp $s;
    return $s;
}

